CREATE DATABASE IF NOT EXISTS `order_system` CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE `order_system`;

CREATE TABLE IF NOT EXISTS users (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '관리자 사용자 고유 ID',
  login_id VARCHAR(50) NOT NULL COMMENT '관리자 로그인 ID',
  password_hash VARCHAR(255) NOT NULL COMMENT '비밀번호 해시값',
  name VARCHAR(50) NOT NULL COMMENT '관리자 이름',
  email VARCHAR(120) NULL COMMENT '이메일 주소',
  role ENUM('ADMIN', 'MANAGER', 'STAFF') NOT NULL DEFAULT 'STAFF' COMMENT '관리자 권한: ADMIN 최고관리자, MANAGER 매니저, STAFF 담당자',
  is_active TINYINT(1) NOT NULL DEFAULT 1 COMMENT '사용 여부: 1 사용, 0 미사용',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
  PRIMARY KEY (id),
  UNIQUE KEY uq_users_login_id (login_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='본사 관리자 사용자';

CREATE TABLE IF NOT EXISTS stores (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '거래처/매장 고유 ID',
  store_uid VARCHAR(50) NOT NULL COMMENT '시스템 내부 거래처 고유키',
  store_code VARCHAR(50) NULL COMMENT '세금계산서 발행/업무용 거래처 코드',
  store_name VARCHAR(120) NOT NULL COMMENT '매장명',
  tax_invoice_code VARCHAR(50) NULL COMMENT '세금계산서 발행코드',
  tax_invoice_name VARCHAR(120) NULL COMMENT '매장명 표시값(기존 세금계산서 발행명 호환 컬럼)',
  machine_vendor VARCHAR(120) NULL COMMENT '머신매입처',
  device_number VARCHAR(120) NULL COMMENT '기기번호',
  owner_name VARCHAR(50) NULL COMMENT '점주/대표자명',
  owner_phone VARCHAR(30) NULL COMMENT '점주/대표자 연락처',
  contact_phone2 VARCHAR(30) NULL COMMENT '추가 연락처',
  business_number VARCHAR(30) NULL COMMENT '사업자등록번호',
  address1 VARCHAR(255) NULL COMMENT '주소1',
  address2 VARCHAR(255) NULL COMMENT '주소2',
  machine_name VARCHAR(120) NULL COMMENT '설치 머신명(표시/호환용)',
  bean_name VARCHAR(120) NULL COMMENT '사용 원두명',
  as_content VARCHAR(255) NULL COMMENT 'A/S 내용',
  closure_status VARCHAR(20) NOT NULL DEFAULT '운영중' COMMENT '영업상태: 운영중, 폐업',
  installed_at DATE NULL COMMENT '설치일자',
  filter_replaced_at DATE NULL COMMENT '필터교체일자',
  payment_type ENUM('PREPAID', 'MONTHLY', 'SPECIAL') NOT NULL DEFAULT 'PREPAID' COMMENT '기본 결제유형: PREPAID 선결제, MONTHLY 월결제, SPECIAL 별도협의',
  memo TEXT NULL COMMENT '거래처/매장 메모',
  is_active TINYINT(1) NOT NULL DEFAULT 1 COMMENT '사용 여부: 1 사용, 0 미사용',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
  PRIMARY KEY (id),
  UNIQUE KEY uq_stores_store_uid (store_uid),
  UNIQUE KEY uq_stores_store_code (store_code),
  KEY idx_stores_store_name (store_name),
  KEY idx_stores_owner_phone (owner_phone)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='거래처/매장 기본 정보';

CREATE TABLE IF NOT EXISTS store_owners (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '점주 계정 고유 ID',
  store_id BIGINT UNSIGNED NOT NULL COMMENT '매장 ID',
  login_id VARCHAR(50) NOT NULL COMMENT '점주 로그인 ID',
  password_hash VARCHAR(255) NOT NULL COMMENT '비밀번호 해시값',
