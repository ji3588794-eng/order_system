CREATE TABLE IF NOT EXISTS payments (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '결제 이력 고유 ID',
  order_id BIGINT UNSIGNED NOT NULL COMMENT '주문 ID',
  payment_method ENUM('BANK_TRANSFER', 'CARD', 'CASH', 'MANUAL', 'ETC') NOT NULL DEFAULT 'BANK_TRANSFER' COMMENT '결제수단: BANK_TRANSFER 계좌이체, CARD 카드, CASH 현금, MANUAL 수동처리, ETC 기타',
  payment_status ENUM('PENDING', 'CONFIRMED', 'CANCELED', 'REFUNDED') NOT NULL DEFAULT 'CONFIRMED' COMMENT '결제 이력 상태: PENDING 확인대기, CONFIRMED 확인완료, CANCELED 취소, REFUNDED 환불',
  amount BIGINT NOT NULL COMMENT '결제 금액',
  payer_name VARCHAR(80) NULL COMMENT '입금자명',
  paid_at DATETIME NULL COMMENT '결제 또는 입금일시',
  provider VARCHAR(30) NULL COMMENT '결제 제공사: TOSS 등',
  provider_payment_key VARCHAR(120) NULL COMMENT '외부 결제 승인 키',
  provider_order_id VARCHAR(120) NULL COMMENT '외부 결제 주문번호',
  provider_transaction_id VARCHAR(120) NULL COMMENT '외부 거래 식별자',
  receipt_url VARCHAR(500) NULL COMMENT '영수증 URL',
  raw_payload JSON NULL COMMENT '결제사 원본 응답',
  accounting_status ENUM('UNMATCHED', 'MATCHED', 'EXCLUDED') NOT NULL DEFAULT 'UNMATCHED' COMMENT '회계/입금 매칭 상태',
  deposit_account_id BIGINT UNSIGNED NULL COMMENT '입금 확인 계좌 ID',
  bank_transaction_id BIGINT UNSIGNED NULL COMMENT '매칭된 은행 거래내역 ID',
  reconciled_at DATETIME NULL COMMENT '정산 매칭 일시',
  memo TEXT NULL COMMENT '결제 메모',
  created_by BIGINT UNSIGNED NULL COMMENT '결제 처리 관리자 ID',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
  PRIMARY KEY (id),
  KEY idx_payments_order_id (order_id),
  KEY idx_payments_paid_at (paid_at),
