) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='점주 마이페이지 계정';

CREATE TABLE IF NOT EXISTS store_inquiries (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '문의 고유 ID',
  store_id BIGINT UNSIGNED NOT NULL COMMENT '매장 ID',
  store_owner_id BIGINT UNSIGNED NULL COMMENT '점주 계정 ID',
  inquiry_type ENUM('ORDER', 'PAYMENT', 'MACHINE', 'ITEM', 'ETC') NOT NULL DEFAULT 'ETC' COMMENT '문의 유형',
  title VARCHAR(200) NOT NULL COMMENT '문의 제목',
  content TEXT NOT NULL COMMENT '문의 내용',
  status ENUM('OPEN', 'ANSWERED', 'CLOSED') NOT NULL DEFAULT 'OPEN' COMMENT '처리 상태',
  answer TEXT NULL COMMENT '관리자 답변',
  answered_by BIGINT UNSIGNED NULL COMMENT '답변 관리자 ID',
  answered_at DATETIME NULL COMMENT '답변 일시',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '문의 생성일시',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
  PRIMARY KEY (id),
  UNIQUE KEY uq_store_inquiries_store_title (store_id, title),
  KEY idx_store_inquiries_store_id (store_id),
  KEY idx_store_inquiries_owner_id (store_owner_id),
  KEY idx_store_inquiries_status (status),
  CONSTRAINT fk_store_inquiries_store FOREIGN KEY (store_id) REFERENCES stores(id),
  CONSTRAINT fk_store_inquiries_owner FOREIGN KEY (store_owner_id) REFERENCES store_owners(id),
  CONSTRAINT fk_store_inquiries_answered_by FOREIGN KEY (answered_by) REFERENCES users(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='점주 문의내역';

