  store_sale_price BIGINT NULL COMMENT '거래처별 출고단가',
  memo TEXT NULL COMMENT '매장별 품목 메모',
  is_active TINYINT(1) NOT NULL DEFAULT 1 COMMENT '사용 여부',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
  PRIMARY KEY (id),
  UNIQUE KEY uq_store_items_store_item_machine (store_id, item_id, machine_id),
  UNIQUE KEY uq_store_items_scope_key (item_scope_key),
  KEY idx_store_items_store_id (store_id),
  KEY idx_store_items_item_id (item_id),
  KEY idx_store_items_machine_id (machine_id),
  CONSTRAINT fk_store_items_store FOREIGN KEY (store_id) REFERENCES stores(id),
  CONSTRAINT fk_store_items_item FOREIGN KEY (item_id) REFERENCES items(id),
  CONSTRAINT fk_store_items_machine FOREIGN KEY (machine_id) REFERENCES machines(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='매장별 취급 품목';

CREATE TABLE IF NOT EXISTS orders (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '주문 고유 ID',
  order_no VARCHAR(50) NOT NULL COMMENT '주문번호',
  store_id BIGINT UNSIGNED NOT NULL COMMENT '매장 ID',
  order_date DATE NOT NULL COMMENT '주문 접수일',
  due_date DATE NULL COMMENT '희망 납기일',
  request_channel ENUM('KAKAO', 'PHONE', 'SMS', 'ADMIN', 'SHOP', 'ETC') NOT NULL DEFAULT 'ADMIN' COMMENT '주문 접수 경로: ADMIN 관리자입력, SHOP 사용자페이지, KAKAO 카카오톡, PHONE 전화, SMS 문자, ETC 기타',
  order_status ENUM('DRAFT', 'STATEMENT_READY', 'PAYMENT_REQUESTED', 'PAYMENT_CONFIRMED', 'MANUAL_APPROVED', 'ORDER_IN_PROGRESS', 'SHIPPED', 'COMPLETED', 'CANCELED') NOT NULL DEFAULT 'DRAFT' COMMENT '주문 상태: DRAFT 임시저장, STATEMENT_READY 명세서작성, PAYMENT_REQUESTED 결제요청, PAYMENT_CONFIRMED 결제확인, MANUAL_APPROVED 관리자예외승인, ORDER_IN_PROGRESS 사입/발주진행, SHIPPED 발송완료, COMPLETED 완료, CANCELED 취소',
  payment_status ENUM('UNPAID', 'PARTIAL_PAID', 'PAID', 'MONTHLY_BILLING', 'MANUAL_APPROVED', 'REFUNDED') NOT NULL DEFAULT 'UNPAID' COMMENT '결제 상태: UNPAID 미결제, PARTIAL_PAID 부분입금, PAID 결제완료, MONTHLY_BILLING 월결제, MANUAL_APPROVED 관리자예외처리, REFUNDED 환불',
  payment_type ENUM('PREPAID', 'MONTHLY', 'SPECIAL') NOT NULL DEFAULT 'PREPAID' COMMENT '거래처 기본 결제유형 snapshot',
  supply_amount BIGINT NOT NULL DEFAULT 0 COMMENT '공급가액 합계',
  vat_amount BIGINT NOT NULL DEFAULT 0 COMMENT '부가세 합계',
  total_amount BIGINT NOT NULL DEFAULT 0 COMMENT '총 결제 요청 금액',
  paid_amount BIGINT NOT NULL DEFAULT 0 COMMENT '결제 완료 금액',
  request_memo TEXT NULL COMMENT '점주 주문 요청 내용',
  admin_memo TEXT NULL COMMENT '관리자 내부 메모',
  manual_approved_by BIGINT UNSIGNED NULL COMMENT '관리자 예외 승인자 ID',
  manual_approved_reason VARCHAR(255) NULL COMMENT '관리자 예외 승인 사유',
  manual_approved_at DATETIME NULL COMMENT '관리자 예외 승인일시',
  created_by BIGINT UNSIGNED NULL COMMENT '주문 생성 관리자 ID',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '생성일시',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '수정일시',
  PRIMARY KEY (id),
  UNIQUE KEY uq_orders_order_no (order_no),
  KEY idx_orders_store_id (store_id),
  KEY idx_orders_order_date (order_date),
  KEY idx_orders_order_status (order_status),
  KEY idx_orders_payment_status (payment_status),
  CONSTRAINT fk_orders_store FOREIGN KEY (store_id) REFERENCES stores(id),
  CONSTRAINT fk_orders_created_by FOREIGN KEY (created_by) REFERENCES users(id),
  CONSTRAINT fk_orders_manual_approved_by FOREIGN KEY (manual_approved_by) REFERENCES users(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='점주 주문 및 명세서';

