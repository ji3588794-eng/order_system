CREATE TABLE IF NOT EXISTS store_items (
  id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '매장별 품목 고유 ID',
  store_id BIGINT UNSIGNED NOT NULL COMMENT '매장 ID',
  item_id BIGINT UNSIGNED NOT NULL COMMENT '품목 ID',
  machine_id BIGINT UNSIGNED NULL COMMENT '연결 머신 ID',
